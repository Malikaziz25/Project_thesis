    private boolean save(){
        if(!validateInputs()){
            return false;
        }
        var prev = Long.valueOf(0);
        if(selectedItem.getStock()!=null){
            prev = selectedItem.getStock();
        }
        var increase = Long.valueOf(txtAddAmount.getText());
        var newqty = prev+increase;
        selectedItem.setStock(newqty);
        var savedEntity = itemRepository.saveAndFlush(selectedItem);
        if(savedEntity!=null){
            selectedItem = savedEntity;           
            JOptionPane.showMessageDialog(null, "Data Tersimpan"); 
            clearFields();
            return true;
        }else{
            JOptionPane.showMessageDialog(null, "Data Tidak Tersimpan");
            return false;
        }
    }
    private void clearFields() {
        
        txtAddAmount.setText("");
        txtCode.setText("");
        lblName.setText("");       
        lblAmount.setText("");       
        lblUnit.setText("");
        lblTotal.setText("");

    }
    private boolean validateInputs(){
        boolean res = true;        
        
        List<String> errors = new ArrayList();
        if(txtAddAmount.getText().trim().length()==0){
            errors.add("Masukkan quantity");            
        }
        if(txtCode.getText().trim().length()==0){
            errors.add( "Masukkan kode");                    
        }
        if(selectedItem==null){
            errors.add( "Masukkan item");            
        }
        
        if(!errors.isEmpty()){
            JOptionPane.showMessageDialog(null, String.join("\n", errors));
            return false;
        }
        return res;
    }

