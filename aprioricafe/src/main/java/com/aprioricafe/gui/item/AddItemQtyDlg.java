    void doSearchCode(){
        var text = txtCode.getText().trim();
        if(text.length()>0){
            Optional<Item> xproduct = itemRepository.findByCode(text);
            if(xproduct.isPresent()){
                selectedItem = xproduct.get();
                lblName.setText(selectedItem.getName());
                lblUnit.setText(selectedItem.getUnitName());
                lblAmount.setText(selectedItem.getStock().toString());
            }else{
                JOptionPane.showMessageDialog(null, "Kode Barang tidak ditemukan"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, "Masukkan kode barang"); 
        }
    }
