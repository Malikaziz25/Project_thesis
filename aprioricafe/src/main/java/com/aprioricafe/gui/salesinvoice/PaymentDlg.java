    void calculateTotal(){
        
        String tdiscountAmount = txtDiscountAmount.getText();
        if(!tdiscountAmount.isBlank()){
            discountAmount = new BigDecimal(tdiscountAmount);
        }
        //grandTotal = salesInvoice.getTotalAmount().subtract(discountAmount);
        grandTotal = salesInvoice.getNetDue().subtract(discountAmount);
        lblGrandTotal.setText(RupiahHelper.formatRupiah(grandTotal));
                
        amountPaid = BigDecimal.ZERO;
        String tamountPaid = txtTotalPaid.getText();
        if(!tamountPaid.isBlank()){
            amountPaid = new BigDecimal(tamountPaid);
        }
        change = amountPaid.subtract(grandTotal);
        lblChange.setText(RupiahHelper.formatRupiah(change));
    }
    
