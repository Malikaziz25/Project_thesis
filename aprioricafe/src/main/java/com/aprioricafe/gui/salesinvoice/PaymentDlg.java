    @Transactional
    boolean savePayment(){        
        if(salesInvoice.getStatus() == SalesInvoiceStatus.UNPAID.getValue()){//first payment
            salesInvoice.setTotalPaid(amountPaid);
            salesInvoice.setDiscountAmount(discountAmount);
            salesInvoice.setGrandTotal(grandTotal);
            var savedSalesInvoice = salesInvoiceSvc.calculatePayments(salesInvoice);
            if(savedSalesInvoice!=null){
                return true;
            }else{
                JOptionPane.showMessageDialog(null, "Data Sales Invoice Tidak Tersimpan");    
            }
        }else{
         //save payment
            if(entity==null){
                entity = new CustomerPayment();
            }        
            entity.setCreatedAt(TimeHelper.getCurrentTimestamp());
            entity.setCreatedBy(appSession.getCurrentuser());
            entity.setTotalAmount(salesInvoice.getTotalAmount());
            entity.setDiscountAmount(discountAmount);
            entity.setGrandTotal(grandTotal);
            if(change.compareTo(BigDecimal.ZERO)<0){//kurang
                entity.setTotalPaid(amountPaid);    
            }else{
                entity.setTotalPaid(grandTotal);    
            }

            entity.setRealTotalPaid(amountPaid);
            entity.setPaymentMethod(PaymentMethod.CASH.getStringValue());        
            entity.setChangeAmount(change);
            entity.setSalesInvoice(salesInvoice);
            //update sales invoice
            //save 
            var saved = customerPaymentRepository.save(entity);
            if(saved!=null){
                var savedSalesInvoice = salesInvoiceSvc.calculatePayments(salesInvoice);
                if(savedSalesInvoice!=null){
                    return true;
                }else{
                    JOptionPane.showMessageDialog(null, "Data Sales Invoice Tidak Tersimpan");    
                }
            }else{
                JOptionPane.showMessageDialog(null, "Data Pembayaran Tidak Tersimpan");    

            }         
        }
        return false;
        
    }
    

